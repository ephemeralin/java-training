package ru.job4j.todolist.model.entity;

/**
 * The interface Entity.
 */
public interface IEntity {
    /**
     * Gets id.
     *
     * @return the id
     */
    int getId();

    /**
     * Sets id.
     *
     * @param id the id
     */
    void setId(int id);

    /**
     * Gets name.
     *
     * @return the name
     */
    String getName();

    /**
     * Sets name.
     *
     * @param name the name
     */
    void setName(String name);
}
